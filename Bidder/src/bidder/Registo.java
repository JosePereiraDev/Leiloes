    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String nome=jTextField1.getText();
        if(nome.equals("")){
            JOptionPane.showMessageDialog(null,
                    "Erro: Preencha o nome de utilizador.", "Erro",
                JOptionPane.ERROR_MESSAGE);
        }
        else{
            try {
                String insert="INSERT INTO bidders (nome,creditos) VALUES ('"+nome+"',100)";
                
                Statement st=bd.connection.createStatement();
                st.executeUpdate(insert);
                st.close();
                initAgent();
            } catch (SQLException ex) {
                if (ex.getMessage().contains("Duplicate entry")){
                    JOptionPane.showMessageDialog(null,
                    "Erro: Utilizador j√° existente, tente outro por favor.", "Erro",
                    JOptionPane.ERROR_MESSAGE);
                    jTextField1.setText("");
                }
                else{
                    Logger.getLogger(Registo.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null,
                    "Erro: Erro and inserir na base de dados. Tente outra vez.", "Erro",
                    JOptionPane.ERROR_MESSAGE);
                    jTextField1.setText("");
                }
            } 
        }
    }//GEN-LAST:event_jButton1MouseClicked

    public void initAgent(){
        String nome=jTextField1.getText();
        try {
            Runtime rt1 = Runtime.getRuntime();
            Runtime rt2 = Runtime.getRuntime();
            String start_platform="java jade.Boot -gui";
            String start_agent="java jade.Boot -container "+nome+":bidder.Bidder ";
            
            Process pr1 = rt1.exec(start_platform);
            Thread.sleep(4000);
            Process pr2 = rt2.exec(start_agent);
        } catch (InterruptedException ex) {
            Logger.getLogger(Registo.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Registo.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            bd.connection.close();
        } catch (SQLException ex) {
            Logger.getLogger(Registo.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }
    
